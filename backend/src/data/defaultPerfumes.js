const defaultPerfumes = brands.flatMap(([brand, tier, basePrice], brandIndex) =>
  scentProfiles.slice(0, 3).map((profile, profileIndex) => {
    const [
      title,
      category,
      concentration,
      size,
      tag,
      notes,
      description,
    ] = profile;
    const offset = profileIndex * 850 + (brandIndex % 5) * 250;
    const price = basePrice + offset;

    return {
      name: `${brand} ${title}`,
      brand,
      price,
      mrp: Math.round(price * 1.22),
      rating: Number((4.1 + ((brandIndex + profileIndex) % 9) / 10).toFixed(1)),
      reviews: 180 + brandIndex * 37 + profileIndex * 113,
      category: tier === "Luxury" && profileIndex === 2 ? "Luxury" : category,
      concentration,
      size,
      tag: tier === "Budget" ? "Budget Pick" : tag,
      image: bottleImages[(brandIndex + profileIndex) % bottleImages.length],
      description: `A ${description} from ${brand}.`,
      notes,
    };
  })
);

export default defaultPerfumes;
